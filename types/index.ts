// نماذج البيانات الأساسية لتطبيق رحلة

export interface مستخدم {
  id: string;
  الاسم: string;
  الصورة_الرمزية?: string;
  تاريخ_الإنشاء: string;
}

export interface رحلة {
  id: string;
  الاسم: string;
  الوصف?: string;
  تاريخ_البداية: string;
  تاريخ_النهاية?: string;
  المشاركون: string[]; // معرفات المستخدمين
  العملة: string;
  المبلغ_المستحق_العام: number; // المبلغ المستحق العام للرحلة
  المبالغ_المستحقة_الفردية: { [معرف_المستخدم: string]: number }; // المبلغ المستحق من كل مشارك
  تاريخ_الإنشاء: string;
  تاريخ_التحديث: string;
}

// نموذج المشتريات الفردية
export interface مشترى_فردي {
  id: string;
  معرف_الرحلة: string;
  معرف_المستخدم: string;
  العنوان: string;
  المبلغ: number;
  الوصف?: string;
  تاريخ_الشراء: string;
  تاريخ_الإنشاء: string;
}

export interface مصروف {
  id: string;
  معرف_الرحلة: string;
  العنوان: string;
  المبلغ: number;
  الفئة: فئة_المصروف;
  دفع_بواسطة: string[]; // معرفات المستخدمين الذين دفعوا
  مبالغ_الدفع: { [معرف_المستخدم: string]: number }; // المبلغ الذي دفعه كل مستخدم
  مشارك_مع: string[]; // معرفات المستخدمين المشاركين في المصروف
  الوصف?: string;
  صورة_الإيصال?: string;
  تاريخ_المصروف: string;
  تاريخ_الإنشاء: string;
}

export type فئة_المصروف =
  | 'طعام'
  | 'مواصلات'
  | 'إقامة'
  | 'ترفيه'
  | 'تسوق'
  | 'صحة'
  | 'أخرى';

export interface رصيد_المستخدم {
  معرف_المستخدم: string;
  إجمالي_المدفوع: number;
  إجمالي_النصيب: number;
  الرصيد_الصافي: number; // موجب = له، سالب = عليه
}

export interface تسوية {
  من: string; // معرف المستخدم
  إلى: string; // معرف المستخدم
  المبلغ: number;
}

export interface ملخص_الرحلة {
  معرف_الرحلة: string;
  إجمالي_المصاريف: number;
  عدد_المصاريف: number;
  أرصدة_المستخدمين: رصيد_المستخدم[];
  التسويات_المطلوبة: تسوية[];
  توزيع_الفئات: { [فئة: string]: number };
}

export interface إعدادات_التطبيق {
  اللغة: 'ar' | 'en';
  الوضع_المظلم: boolean;
  العملة_الافتراضية: string;
  الإشعارات_مفعلة: boolean;
}

// أنواع للتنقل
export type معاملات_الشاشات = {
  الرئيسية: undefined;
  تفاصيل_الرحلة: { معرف_الرحلة: string };
  إضافة_رحلة: undefined;
  تعديل_الرحلة: { معرف_الرحلة: string };
  إضافة_مصروف: { معرف_الرحلة: string };
  تعديل_مصروف: { معرف_الرحلة: string; معرف_المصروف: string };
  التحليلات: { معرف_الرحلة: string };
  الإعدادات: undefined;
  إدارة_المستخدمين: undefined;
};
